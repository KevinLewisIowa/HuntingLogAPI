class UsersController < ApplicationController
  before_action :set_user, only: [:show, :update, :destroy]

  # GET /users
  def index
    @users = User.all

    render json: @users
  end

  # GET /users/1
  def show
    render json: @user
  end

  # POST /users
  def create
    @userobject = User.find_by(username: params[:username]);
    if @userobject
      render 'existing user'
    else
      @user = User.new(user_params)

      if @user.save
        render json: @user, status: :created, location: @user
      else
        render json: @user.errors, status: :unprocessable_entity
      end
    end
  end

  # PATCH/PUT /users/1
  def update
    @sessiontoken = request.headers["sessiontoken"] #this can be used places to get the session token, as long as I pass it in the headers
    if @user.update(user_params)
      render json: @user
    else
      render json: @user.errors, status: :unprocessable_entity
    end
  end

  # DELETE /users/1
  def destroy
    @user.destroy
  end
  
  # this is for logging in
  def login
    @user = User.find_by(username: params[:username]);
    if @user && @user.authenticate(params[:password])
      o = [('a'..'z'), ('A'..'Z')].map { |i| i.to_a }.flatten
      string = (0...50).map { o[rand(o.length)] }.join
      @user.update_attribute(:session_token, string)
      @user.save
      render json: @user
    else
      render json: 'bad login'
    end
  end
  
  def getUserByToken
    @userobject = User.find_by(session_token: params[:sessionToken])
    
    render json: @userobject
  end
  
  def logout
    # erase the user's session token, need to add the route
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_user
      @user = User.find(params[:id])
    end

    # Only allow a trusted parameter "white list" through.
    def user_params
      params.require(:user).permit(:username, :email, :password, :session_token)
    end
end
